package conditionalStatements;

public class TernaryShortHandMethodForConditionalStatement {

	public static void main(String[] args) {
		
		int a = 23;
		int b = 105;
		
		int maxOfBothTheNumber = 0;
		
		maxOfBothTheNumber = a>b ? a : b;

		
		System.out.println("Max Of Both The Number is : " + maxOfBothTheNumber);
	}

}
