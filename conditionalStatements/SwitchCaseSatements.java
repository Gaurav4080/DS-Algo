package conditionalStatements;

public class SwitchCaseSatements {

	public static void main(String[] args) {
		
		int dayOfWeek = 2; 
		
		switch (dayOfWeek) {
		case 1 :
			System.out.println("I am On Break");
			break;
		case 2:
			System.out.println("Iam working");
			break;
		case 3:
			System.out.println("Iam playing Football");
		default:
			System.out.println("Enjoy your day");
	}
	}
}
