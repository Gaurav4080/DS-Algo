package inheritance;

public class PersonClass {
		
		protected String name;
		
		public void walk () {
			System.out.println("person " + name + " is walking");
		}
		
		public void eat (){
			System.out.println("person " + name + " is eating");
		}
		
		public static void laughing() {
			System.out.println("Person is Laughing");
		}
}
